from __future__ import annotations

import re


def generate_vpn_support_reply(
    text: str,
    *,
    service_name: str,
    support_contact: str,
) -> str | None:
    normalized = _normalize(text)
    if not normalized:
        return None

    if _contains_any(normalized, ("спасибо", "благодарю", "понял", "ок", "окей")):
        return None

    signature = f"\n\nЕсли не получится, напишите {support_contact}."

    if _contains_any(normalized, ("привет", "здравствуйте", "добрый", "доброе", "хай")):
        return (
            f"Здравствуйте! Это поддержка {service_name}. "
            "Напишите, что нужно: подключить VPN, продлить доступ, настроить устройство или решить проблему с соединением."
        )

    if _contains_any(normalized, ("оплат", "купить", "стоим", "цена", "тариф", "продл", "подпис")):
        return (
            f"По оплате и продлению {service_name}: напишите, на какой срок нужен доступ и для какого устройства. "
            "Я подскажу дальнейшие шаги."
            f"{signature}"
        )

    if _contains_any(normalized, ("не работает", "не подключ", "ошибка", "отвал", "перестал", "не груз", "не откры")):
        return (
            "Давайте быстро проверим VPN:\n"
            "1. Полностью выключите VPN и включите снова.\n"
            "2. Переключите сервер/локацию, если есть выбор.\n"
            "3. Проверьте, открываются ли сайты без VPN.\n"
            "4. Перезагрузите приложение VPN.\n\n"
            "Если ошибка останется, пришлите скриншот ошибки и напишите устройство: iPhone, Android, Windows или macOS."
            f"{signature}"
        )

    if _contains_any(normalized, ("iphone", "айфон", "ios", "ipad", "айпад")):
        return (
            "Для iPhone/iPad обычно нужно:\n"
            "1. Установить приложение, которое вам выдали для VPN.\n"
            "2. Импортировать ссылку или конфиг.\n"
            "3. Разрешить добавление VPN-профиля в iOS.\n"
            "4. Нажать подключение в приложении.\n\n"
            "Если пришлёте скрин, где остановились, подскажу точнее."
            f"{signature}"
        )

    if _contains_any(normalized, ("android", "андроид", "samsung", "xiaomi", "honor", "huawei")):
        return (
            "Для Android обычно нужно установить VPN-приложение, импортировать ссылку/конфиг и нажать подключение. "
            "Если приложение просит разрешение на VPN-подключение, его нужно подтвердить.\n\n"
            "Напишите модель телефона и что именно показывает приложение."
            f"{signature}"
        )

    if _contains_any(normalized, ("windows", "виндов", "компьютер", "пк", "ноут")):
        return (
            "Для Windows проверьте:\n"
            "1. Приложение VPN запущено от обычного пользователя или администратора.\n"
            "2. Конфиг/ссылка импортированы без ошибок.\n"
            "3. Антивирус или firewall не блокирует приложение.\n"
            "4. Время и дата на компьютере выставлены правильно.\n\n"
            "Пришлите текст ошибки или скриншот, если подключение не проходит."
            f"{signature}"
        )

    if _contains_any(normalized, ("mac", "macos", "макбук", "мак")):
        return (
            "Для macOS: установите VPN-приложение, импортируйте ссылку/конфиг и разрешите VPN-профиль в системных настройках. "
            "Если macOS блокирует приложение, откройте настройки безопасности и разрешите запуск."
            f"{signature}"
        )

    if _contains_any(normalized, ("медлен", "скорость", "тормоз", "лага", "пинг")):
        return (
            "Если VPN работает медленно:\n"
            "1. Переключите сервер/локацию.\n"
            "2. Проверьте скорость интернета без VPN.\n"
            "3. Перезапустите приложение VPN.\n"
            "4. Если есть выбор протокола, попробуйте другой.\n\n"
            "Напишите вашу страну/оператора и устройство, подберём лучший вариант."
            f"{signature}"
        )

    if _contains_any(normalized, ("конфиг", "ссылка", "ключ", "qr", "куар", "настрой")):
        return (
            "Чтобы выдать или восстановить конфиг VPN, напишите устройство и срок доступа. "
            "Если у вас уже есть ссылка/QR, пришлите, какая ошибка появляется при импорте."
            f"{signature}"
        )

    if _contains_any(normalized, ("возврат", "вернуть деньги", "refund")):
        return (
            "По возврату напишите дату оплаты, сумму и причину. "
            "Я передам запрос на проверку и вернусь с ответом."
            f"{signature}"
        )

    if "?" in text or _contains_any(normalized, ("как", "что", "где", "почему", "можно", "нужно", "помог")):
        return (
            f"Я поддержка {service_name}. Уточните, пожалуйста, что именно нужно: подключение, оплата, продление, настройка устройства или ошибка VPN. "
            "Так я быстрее дам точную инструкцию."
            f"{signature}"
        )

    return None


def _normalize(text: str) -> str:
    return re.sub(r"\s+", " ", text.casefold()).strip()


def _contains_any(text: str, needles: tuple[str, ...]) -> bool:
    return any(needle in text for needle in needles)
